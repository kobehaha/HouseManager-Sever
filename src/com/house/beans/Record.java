package com.house.beans;

public class Record {
	private String buyerID;
	private String houseId;
	private String url;
	private int sellerAccount;
	private int sellerName;
	private String timeOk;
	private int price;
	private int time;

	public String getBuyerID() {
		return buyerID;
	}

	public void setBuyerID(String buyerID) {
		this.buyerID = buyerID;
	}

	public String getHouseId() {
		return houseId;
	}

	public void setHouseId(String houseId) {
		this.houseId = houseId;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public int getSellerAccount() {
		return sellerAccount;
	}

	public void setSellerAccount(int sellerAccount) {
		this.sellerAccount = sellerAccount;
	}

	public int getSellerName() {
		return sellerName;
	}

	public void setSellerName(int sellerName) {
		this.sellerName = sellerName;
	}

	public String getTimeOk() {
		return timeOk;
	}

	public void setTimeOk(String timeOk) {
		this.timeOk = timeOk;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

	public int getTime() {
		return time;
	}

	public void setTime(int time) {
		this.time = time;
	}

}
